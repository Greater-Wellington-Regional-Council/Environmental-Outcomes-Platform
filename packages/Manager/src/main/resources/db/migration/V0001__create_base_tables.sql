CREATE TABLE
  council_boundaries (
    id INTEGER NOT NULL,
    NAME VARCHAR NOT NULL,
    geom GEOMETRY NOT NULL,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (id)
  );

CREATE TABLE
  whaitua_boundaries (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY,
    NAME VARCHAR NOT NULL,
    geom GEOMETRY NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (id)
  );

CREATE TABLE
  sites (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY,
    siteid_hilltop INTEGER NOT NULL UNIQUE,
    NAME VARCHAR NOT NULL,
    geom GEOMETRY NOT NULL,
    PRIMARY KEY (id)
  );

CREATE TABLE
  rivers (
    hydro_id INTEGER NOT NULL,
    next_hydro_id INTEGER,
    nz_segment INTEGER NOT NULL,
    is_headwater BOOLEAN NOT NULL,
    stream_order INTEGER NOT NULL,
    geom geometry NOT NULL,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (hydro_id)
  );

CREATE INDEX
  ON rivers USING gist (geom);

CREATE TABLE
  watersheds (
    hydro_id INTEGER NOT NULL,
    nz_segment INTEGER NOT NULL,
    geom geometry NOT NULL,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (hydro_id)
  );

CREATE INDEX
  ON watersheds USING gist (geom);

CREATE TABLE
  catchments (
    hydro_id INTEGER NOT NULL,
    geom geometry NOT NULL,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (hydro_id)
  );

CREATE TABLE
  minimum_flow_limits (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY,
    plan_description VARCHAR NOT NULL,
    plan_management_point_name VARCHAR NOT NULL,
    plan_minimum_flow_value NUMERIC NOT NULL,
    plan_minimum_flow_unit VARCHAR NOT NULL,
    siteid_hilltop INTEGER NOT NULL,
    plan_table NUMERIC NOT NULL,
    plan_version VARCHAR NOT NULL,
    hydro_id INTEGER NOT NULL,
    FOREIGN KEY (siteid_hilltop) REFERENCES sites (siteid_hilltop),
    PRIMARY KEY (id)
  );

CREATE TABLE
  groundwater_zones (
    id VARCHAR NOT NULL,
    NAME VARCHAR NOT NULL,
    category VARCHAR NOT NULL,
    DEPTH VARCHAR NOT NULL,
    notes VARCHAR NOT NULL,
    geom GEOMETRY NOT NULL,
    catchment_id INTEGER,
    site_id INTEGER,
    groundwater_zone VARCHAR UNIQUE,
    PRIMARY KEY (id)
  );

CREATE TABLE
  allocation_amounts (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY,
    area_description VARCHAR NOT NULL,
    hydro_id INTEGER,
    allocation_amount NUMERIC NOT NULL,
    allocation_amount_unit VARCHAR NOT NULL,
    consented_allocation_greater_than_allocation_amount BOOLEAN NOT NULL,
    catchment_management_unit VARCHAR NOT NULL,
    groundwater_zone VARCHAR NOT NULL,
    plan_version VARCHAR NOT NULL,
    plan_table NUMERIC NOT NULL,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (id)
  );

CREATE TABLE
  raw_rec_features_rivers (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY,
    ingested_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    DATA jsonb,
    PRIMARY KEY (id)
  );

COMMENT
  ON TABLE raw_rec_features_rivers IS 'Store for the REC rivers as RAW GeoJSON features. One Feature per row';

CREATE TABLE
  raw_rec_features_watersheds (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY,
    ingested_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    DATA jsonb,
    PRIMARY KEY (id)
  );

COMMENT
  ON TABLE raw_rec_features_rivers IS 'Store for the REC watersheds as RAW GeoJSON features. One Feature per row';

CREATE TABLE
  raw_rec_rivers_names (
    nz_segment INTEGER NOT NULL,
    river_name VARCHAR NOT NULL,
    PRIMARY KEY (nz_segment)
  );
